def get_dtc_description(code: str) -> str:
    """Get the description for a DTC code."""
    return DTC_DESCRIPTIONS.get(code, "Unknown DTC")


def decode_dtc_bytes(byte1: int, byte2: int) -> str:
    """Decode 2 raw DTC bytes into a DTC string.

    OBD-II DTC encoding (2 bytes):
      Byte 1: [C1 C0 D1 D1 D1 D1 D0 D0]
        Bits 7-6: System type (00=P, 01=C, 10=B, 11=U)
        Bits 5-4: Second character (0-3)
        Bits 3-0: Third character (0-F)
      Byte 2: [D3 D3 D3 D3 D2 D2 D2 D2]
        Bits 7-4: Fourth character (0-F)
        Bits 3-0: Fifth character (0-F)

    Example: 0x04, 0x20 -> P0420
    """
    system_type = (byte1 >> 6) & 0x03
    second_char = (byte1 >> 4) & 0x03
    third_char = byte1 & 0x0F
    fourth_char = (byte2 >> 4) & 0x0F
    fifth_char = byte2 & 0x0F

    system_map = {0: 'P', 1: 'C', 2: 'B', 3: 'U'}
    prefix = system_map.get(system_type, 'P')

    return f"{prefix}{second_char}{third_char:X}{fourth_char:X}{fifth_char:X}"
